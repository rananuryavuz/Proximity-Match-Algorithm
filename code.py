def proximity_match(a,b):
    X = (keyboard_cartesian[a]['x'] - keyboard_cartesian[b]['x'])**2
    Y = (keyboard_cartesian[a]['y'] - keyboard_cartesian[b]['y'])**2
    if math.sqrt(X+Y) < 1.01:
      score = 80
    elif math.sqrt(X+Y) < 1.42 and math.sqrt(X+Y) >= 1.01:
      score = 70
    else:
      score = 0
    return score
     
